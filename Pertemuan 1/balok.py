print("Menghitung luas dan volume balok")

# Variabel
panjang = 20
lebar = 10
tinggi = 2

# Rumus
luas = (2 * panjang * lebar) + (2 * panjang * tinggi) + (2 * lebar * tinggi)
volume = panjang * lebar * tinggi

# Output
print("Masukkan panjang: ",panjang)
print("Masukkan lebar: ",lebar)
print("Masukkan tinggi",tinggi)
print("Luas = ",luas)
print("Volume = ",volume)